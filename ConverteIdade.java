
public class ConverteIdade {

	public static void main(String[] args) {
		int idadeAnos = 21;
		int idadeMeses = 4;
		
		int idadeAnosEmDias = idadeAnos * 365;
		int idadeMesesEmDias = idadeMeses * 30;
		
		int idadeEmDias = idadeAnosEmDias + idadeMesesEmDias;
		System.out.println(idadeEmDias);
		

	}

}
