
public class AntecessorSucessor {

	public static void main(String[] args) {
		int antecessor;
		int sucessor;
		int valor = 25;
		
		System.out.println("O valor é "+ valor);
		
		antecessor = valor-1;
		sucessor = valor+1;
		
		System.out.println("O antecessor é " + antecessor);
		System.out.println("O sucessor é " + sucessor);
	}

}
